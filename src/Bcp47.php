    /**
     * Normalize given tag and returns subtags.
     *
     * Normalization includes:
     *  - Preferred values replacement when available
     *  - Case normalization (all lowercase, but uppercase region and title-case script)
     *  - Replacement of numeric region codes with 2-chars alpha code when available
     *  - Replacement of 3-chars language code (ISO 639-3) with 2-chars code (ISO 636-1) whe n
     *    available
     *
     * All subtags are always present in the returned array, they are an empty string when not
     * present in the tag. An invalid tag result in an array with all empty-string subtags.
     *
     * @param string $tag
     * @return BCP_47_Tag_Data
     */
    public static function splitTag(string $tag): array
    {
        $tag = static::normalizeTagString($tag);

        $preferred = static::maybePreferred($tag);
        if ($preferred !== null) {
            return $preferred;
        }

        preg_match(self::BCP47_REGEX, $tag, $matches);
        $matches = array_filter($matches);
        if ($matches === []) {
            return static::emptyData();
        }

        $grandfathered = static::maybeGrandfathered($matches['grandfathered'] ?? '');
        if ($grandfathered !== null) {
            return $grandfathered;
        }

        $language = static::maybeRedoLanguage($matches['language'] ?? '', $tag);
        if (is_array($language)) {
            return $language;
        }

        $script = static::filterScript($matches['script'] ?? '');
        if ($script === null) {
            return static::emptyData();
        }

        $region = static::filterRegion($matches['region'] ?? '');
        if ($region === null) {
            return static::emptyData();
        }

        $variant = static::filterVariant($matches['variant'] ?? '', $language, $script, $region);
        if ($variant === null) {
            return static::emptyData();
        }

        return [
            self::GRANDFATHERED => '',
            self::LANGUAGE => $language,
            self::LANGUAGE_EXTENSION => $matches['extLang'] ?? '',
            self::SCRIPT => $script,
            self::REGION => $region,
            self::VARIANT => $variant,
            self::EXTENSION => $matches['extension'] ?? '',
            self::PRIVATE_USE => $matches['privateUse'] ?? '',
        ];
    }

    /**
     * Return the normalized version of the tag or null for invalid tags.
     *
     * Normalization includes:
     * - Preferred values replacement when available
     * - Case normalization (all lowercase, but uppercase region and title-case script)
     * - Script suppression when redundant
     * - Replacement of numeric region codes with 2-chars alpha code when available
     * - Replacement of 3-chars language code (ISO 639-3) with 2-chars code (ISO 636-1) when
     *   available
     *
     * @param string $tag
     * @return string|null
     */
    public static function filterTag(string $tag): ?string
    {
        $subTags = static::splitTag($tag);

        if ($subTags[self::GRANDFATHERED] !== '') {
            return $subTags[self::GRANDFATHERED];
        }

        if ($subTags[self::LANGUAGE] === '') {
            return null;
        }

        $filteredSubTags = [];
        foreach (self::KEYS as $key) {
            /** @psalm-suppress PossiblyUndefinedArrayOffset */
            (($subTags[$key] ?? '') !== '') and $filteredSubTags[] = $subTags[$key];
        }

        return implode('-', $filteredSubTags);
    }

    /**
     * Whether the given string is a valid IETF BCP 47 tag
     *
     * @param string $tag
     * @return bool
     */
    public static function isValidTag(string $tag): bool
    {
        return static::filterTag($tag) !== null;
    }

    /**
     * Whether the given IETF BCP 47 tag is for a right-to-left locale.
     *
     * Please note that some languages exists in different scripts, both RTL and LTR
     * (e.g. Latin and Arabic). In those cases, if the script is not provided this
     * function returns false, even if a region that might give indication of the script is
     * provided.
     * For example, this function will return false for 'az-IR' (Azerbaijani spoken in Iran)
     * even if Azerbaijani in Iran uses Arab script.
     * It will return true for 'az-Arab' or 'az-Arab-IR'.
     * It correctly returns true for languages that only support RTL scripts, such us Hebrew.
     *
     * Note this also returns false if the given tag is invalid.
     *
     * @param string $tag
     * @return bool
     */
    public static function isRtl(string $tag): bool
    {
        $subTags = static::splitTag($tag);

        if ($subTags[self::LANGUAGE] === '') {
            return false;
        }

        if ($subTags[self::SCRIPT] !== '') {
            return in_array($subTags[self::SCRIPT], self::RTL_SCRIPTS, true);
        }

        return in_array($subTags[self::LANGUAGE], self::RTL_LANGS, true);
    }

    /**
     * @param string $tag
     * @return string
     */
    private static function normalizeTagString(string $tag): string
    {
        $tag = strtolower(preg_replace('{\s+}', '', $tag));
        $tag = strtr($tag, self::SUPPRESS_SCRIPT);

        return preg_replace(self::REPLACE_EXTLANG_REGEX, '$1$2', $tag) ?? '';
    }

    /**
     * @param string $tag
     * @return BCP_47_Tag_Data|null
     */
    private static function maybePreferred(string $tag): ?array
    {
        $preferredValue = self::PREFERRED_VALUES['tag'][$tag] ?? null;
        if ($preferredValue !== null) {
            $data = static::emptyData();
            is_string($preferredValue) and $preferredValue = [self::LANGUAGE => $preferredValue];
            /** @var BCP_47_Tag_Data */
            return array_replace($data, $preferredValue);
        }

        return null;
    }

    /**
     * @param string $grandfathered
     * @return BCP_47_Tag_Data|null
     */
    private static function maybeGrandfathered(string $grandfathered): ?array
    {
        if ($grandfathered === '') {
            return null;
        }

        $data = static::emptyData();
        $data[self::GRANDFATHERED] = $grandfathered;

        return $data;
    }

    /**
     * @param string $languageRaw
     * @param string $tag
     * @return BCP_47_Tag_Data|string
     */
    private static function maybeRedoLanguage(string $languageRaw, string $tag): array|string
    {
        $language = static::filterLanguage($languageRaw);
        if ($language === null) {
            return static::emptyData();
        }

        if ((strtolower($language) !== $languageRaw) && str_starts_with($tag, $languageRaw)) {
            $tag = $language . substr($tag, strlen($languageRaw));

            return static::splitTag($tag);
        }

        return $language;
    }

    /**
     * @param string $language
     * @return string|null
     */
    private static function filterLanguage(string $language): ?string
    {
        if (array_key_exists($language, self::PREFERRED_VALUES['language'])) {
            return self::PREFERRED_VALUES['language'][$language];
        }

        if (preg_match('{^q[a-t][a-z]$}', $language)) {
            return $language;
        }

        if (strlen($language) >= 4) {
            return in_array($language, self::REGISTERED_LANGUAGES, true) ? $language : null;
        }

        $isIsoShort = in_array($language, self::ISO_639, true);
        $isIsoLong = array_key_exists($language, self::ISO_639);
        if (!$isIsoShort && !$isIsoLong) {
            return null;
        }

        if ($isIsoLong && (self::ISO_639[$language] !== null)) {
            $language = self::ISO_639[$language];
        }

        return $language;
    }

    /**
     * @param string $script
     * @return string|null
     */
    private static function filterScript(string $script): ?string
    {
        if ($script === '') {
            return '';
        }

        if (preg_match('{^(?:qa[ab][a-x])$}', $script) === 1) {
            return ucfirst($script);
        }

        return in_array($script, self::SCRIPTS, true) ? ucfirst($script) : null;
    }

    /**
     * @param string $region
     * @return string|null
     */
    private static function filterRegion(string $region): ?string
    {
        if ($region === '') {
            return '';
        }

        if (array_key_exists($region, self::PREFERRED_VALUES['region'])) {
            return self::PREFERRED_VALUES['region'][$region];
        }

        if (is_numeric($region)) {
            $alpha = array_search($region, self::REGIONS_MAP, true);
            if (is_string($alpha)) {
                return $alpha;
            }

            if (!in_array($region, self::REGIONS_NUM, true)) {
                return null;
            }

            return $region;
        }

        $region = strtoupper($region);

        if (preg_match('{^(?:X[A-Z])|(?:Q[M-Z])$}', $region) === 1) {
            return $region;
        }

        return in_array($region, self::REGIONS_ALPHA, true) ? $region : null;
    }

    /**
     * @param string $variant
     * @param string $lang
     * @param string $script
     * @param string $region
     * @return string|null
     */
    private static function filterVariant(
        string $variant,
        string $lang,
        string $script,
        string $region
    ): ?string {

        if ($variant === '') {
            return '';
        }

        $search = ["{$lang}-{$variant}"];
        if ($region !== '') {
            $region = strtolower($region);
            $search[] = "{$lang}-{$region}-{$variant}";
        }
        if ($script !== '') {
            $script = strtolower($script);
            $search[] = "{$lang}-{$script}-{$variant}";
            if ($region !== '') {
                $search[] = "{$lang}-{$script}-{$region}-{$variant}";
            }
        }

        foreach ($search as $test) {
            if (in_array($test, self::REGISTERED_VARIANTS, true)) {
                return $variant;
            }
        }

        return null;
    }

    /**
     * @return BCP_47_Tag_Data
     */
    private static function emptyData(): array
    {
        /** @var BCP_47_Tag_Data */
        return array_fill_keys(self::KEYS, '');
    }
}
